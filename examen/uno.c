void examen(unsigned char T[])
{
	unsigned short tot = T[12] << 8 | T[13];
	
	if(tot == 2048) //Si es IP
	{
		if( (14 + ((T[14]&15)*4) - 1) > 33 ) //Si tiene opciones
		{
			//1.-
			if(T[23] == 1) //Si es ICMP
			{
				printf("\n\t>> Trama: %d <<",i+1);
				printf("\nOpciones: ");
				printf("%.2x",(T[34]<<24) | (T[35]<<16) | (T[36]<<8) | (T[37]));
			}
			
			//2.-
			else if(T[23] == 17) //Si es UDP
			{
				printf("\n\t>> Trama: %d <<",i+1);
				printf("\nFragment Offset: %d unidades de 8 bytes", (T[20]&31)<<8 | T[21]);
			}
		}
	}
	
	if(tot <= 1500) // Si es LLC
	{
		//3.-
		if((T[16] & 3) == 0 || (T[16] & 3) == 2) //Informacion
		{
			printf("\n\t>> Trama: %d <<",i+1);
			printf("\nN(s) = %d, N(r) = %d",T[16]>>1,T[17]>>1);
		}
		
		//5.-
		if((T[16] & 3) == 3) //No numerada
		{
			if(T[16]&16)
			{
				if(! (T[15] & 1) )	//resp
				{
					if((((T[16]>>2)&3) | ((T[16]>>3)&28)) == 8 ) //DISC
					{
						printf("\n\t>> Trama: %d <<",i+1);
						printf("\nMAC Origen: %.2x:%.2x:%.2x:%.2x:%.2x:%.2x", T[6], T[7], T[8], T[9], T[10], T[11]);
					}
				}
			}
		}
		
		//6.-
		if((T[16] & 3) == 1) //Supervision
		{
			if(((T[16]>>2) & 3) == 2) //REJ
			{
				printf("\n\t>> Trama: %d <<",i+1);
				printf("\nN(r) = %d",T[17]>>1);
			}
		}
				
	}
	
	//4.-
	if(tot == 2054)
	{
		if((T[14]<<8 | T[15]) == 16)
		{
			if( (T[20] <<8 | T[21]) == 4 )
			{
				printf("\n\t>> Trama: %d <<",i+1);
				printf("\nHW Destino: %.2x:%.2x:%.2x:%.2x:%.2x:%.2x",T[32],T[33],T[34],T[35],T[36],T[37]);
			}
		}
	}
	
	printf("\n");
}
